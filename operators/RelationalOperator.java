package com.operators;

public class RelationalOperator {

	public static void main(String[] args) {
		int a,b;
		a=10;
		b=21;
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
