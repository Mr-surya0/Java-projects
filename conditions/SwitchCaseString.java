package com.conditions;

public class SwitchCaseString {
	
		public static void main(String args[]){
			String s="surya";
			switch (s) {
			case "murugan":
				System.out.println("hii murugan");
				break;
			case "muthu":
				System.out.println("hii amithab");
				break;
			case "surya":
				System.out.println("hii surya");
				break;
				default:
			
			}
		}
	}

